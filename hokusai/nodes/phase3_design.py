_DESIGN_START_MARKERS = [
    "## 設計チェック",
    "## 設計レビュー",
    "## Design Check",
]

_DESIGN_REQUIRED_SECTIONS = [
    "### 概要",
    "### 設計方針",
    "### リスク",
    "### 確認事項",
]

_FORBIDDEN_META_PHRASES = [
    "手動でNotionに貼り付け",
    "Notion MCPにはページコンテンツ更新ツールがない",
    "手動で貼り付けてください",
    "Notion追記について",
]


def _validate_design_output(raw_output: str) -> None:
    """raw_output がフルレポートであることを検証

    検証条件:
    1. 許可開始見出しで始まること
    2. 必須セクション見出しを 4 つ中 3 つ以上含むこと
    3. 禁止メタ説明を含まないこと

    失敗時は RuntimeError を送出し、Phase 3 を fail-fast にする。
    """
    if not raw_output or not raw_output.strip():
        raise RuntimeError(
            "Phase 3 出力検証失敗: raw_output が空です。"
        )

    stripped = raw_output.strip()
    starts_with_marker = any(stripped.startswith(m) for m in _DESIGN_START_MARKERS)
    if not starts_with_marker:
        first_line = stripped.split("\n", 1)[0][:80]
        raise RuntimeError(
            f"Phase 3 出力検証失敗: 許可開始見出しで始まっていません。"
            f" 先頭行={first_line!r}"
        )

    for phrase in _FORBIDDEN_META_PHRASES:
        if phrase in raw_output:
            raise RuntimeError(
                f"Phase 3 出力検証失敗: 禁止メタ説明を含んでいます。"
                f" phrase={phrase!r}"
            )

    section_count = sum(1 for s in _DESIGN_REQUIRED_SECTIONS if s in raw_output)
    if section_count < 3:
        raise RuntimeError(
            f"Phase 3 出力検証失敗: フルレポートの必須セクションが不足しています。"
            f" 必須セクション={section_count}/4 (閾値=3)"
        )

    logger.info(f"Phase 3 出力検証OK: 必須セクション={section_count}/4")


# ---------------------------------------------------------------------------
# 保存後検証
# ---------------------------------------------------------------------------

def _verify_design_subpage_content(state: WorkflowState, raw_output: str) -> None:
    """子ページ本文が確実に保存されていることを検証

    検証項目:
    1. state["phase_subpages"][3] が存在する
    2. 子ページ本文が空でない
    3. 子ページ本文の長さが raw_output の 30% 以上
    4. raw_output 中盤から抽出したアンカーが子ページに含まれる

    失敗時は RuntimeError を送出し、Phase 3 を失敗扱いにする。
    """
    if os.environ.get("HOKUSAI_SKIP_NOTION") == "1":
        return

    subpage_url = state.get("phase_subpages", {}).get(3)
    if not subpage_url:
        raise RuntimeError(
            "Phase 3 本文検証失敗: 子ページURLが未登録です。"
        )

    # リトライあり: Notion MCP タイムアウト対策
    import time

    from ..integrations.notion_mcp import NotionMCPClient

    max_attempts = 3
    retry_delay = 5  # 秒
    page_content = None
    last_error = None

    for attempt in range(1, max_attempts + 1):
        try:
            notion = NotionMCPClient()
            page_content = notion.get_page_content(subpage_url)
            break
        except Exception as e:
            last_error = e
            if attempt < max_attempts:
                logger.warning(
                    f"Phase 3 本文取得リトライ {attempt}/{max_attempts}: {e}"
                )
                time.sleep(retry_delay)
            else:
                raise RuntimeError(
                    f"Phase 3 本文検証失敗: 子ページ本文の取得に失敗しました"
                    f" ({max_attempts}回試行): {last_error}"
                )

    if not page_content or not page_content.strip():
        raise RuntimeError(
            "Phase 3 本文検証失敗: 子ページ本文が空です。"
        )

    raw_len = len(raw_output.strip())
    page_len = len(page_content.strip())
    if raw_len > 0 and page_len < raw_len * 0.3:
        raise RuntimeError(
            f"Phase 3 本文検証失敗: 子ページ本文が短すぎます。"
            f" raw={raw_len}文字, page={page_len}文字"
            f" (比率={page_len / raw_len:.0%}, 閾値=30%)"
        )

    anchor = _pick_body_anchor(raw_output)
    if anchor and anchor not in page_content:
        raise RuntimeError(
            f"Phase 3 本文検証失敗: 子ページに本文アンカーが見つかりません。"
            f" アンカー={anchor[:60]!r}"
        )

    logger.info(
        f"Phase 3 本文検証OK: 子ページに本文が保存されています"
        f" (raw={raw_len}, page={page_len})"
    )


def _pick_body_anchor(raw_output: str) -> str | None:
    """raw_output の中盤から非見出し行を抽出してアンカーにする。"""
    lines = raw_output.strip().splitlines()
    content_lines = [
        line.strip()
        for line in lines
        if line.strip()
        and not line.strip().startswith("#")
        and len(line.strip()) >= 10
    ]
    if not content_lines:
        return None
    mid_idx = len(content_lines) // 2
    return content_lines[mid_idx][:80]


