def _build_design_check_prompt(
    task_url: str,
    research_result: str,
    cross_review_context: str = "",
) -> str:
    """Phase 3 設計チェック用の直接プロンプトを構築する

    slash skill ではなく execute_prompt() で直接実行するためのプロンプト。
    出力フォーマットをプロンプト本文に埋め込むことで、
    前置き文や要約の混入を防ぐ。
    """
    from ..prompts import get_prompt

    cross_review_section = ""
    if cross_review_context:
        cross_review_section = (
            "\n## Phase 2 クロスLLMレビュー指摘事項\n\n"
            "以下は Phase 2 事前調査に対するクロスレビューで指摘された事項です。\n"
            "設計チェック時にこれらの指摘を考慮してください。\n\n"
            f"{cross_review_context}\n"
        )

    return get_prompt(
        "phase3.design_check",
        task_url=task_url,
        research_result=research_result,
        cross_review_section=cross_review_section,
    )


def _build_design_retry_prompt(
    task_url: str,
    research_result: str,
    previous_output: str,
    validation_error: str,
    cross_review_context: str = "",
) -> str:
    """Phase 3 出力検証失敗時の再生成プロンプトを構築する。

    前回出力が仕様違反だったことを明示し、元タスク情報と調査結果を
    再注入した上で全文を規定フォーマットで再出力させる。
    """
    from ..prompts import get_prompt

    cross_review_section = ""
    if cross_review_context:
        cross_review_section = (
            f"\n## Phase 2 クロスLLMレビュー指摘事項\n\n"
            f"{cross_review_context}\n"
        )

    return get_prompt(
        "phase3.design_check_retry",
        task_url=task_url,
        research_result=research_result,
        previous_output=previous_output,
        validation_error=validation_error,
        cross_review_section=cross_review_section,
    )
