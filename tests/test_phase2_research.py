class TestInsertAfterExistingMarker:
    """after_marker 指定時にマーカーがプロンプトに含まれることを検証"""

    def _make_client(self):
        from hokusai.integrations.notion_mcp import NotionMCPClient
        client = NotionMCPClient()
        mock_claude = MagicMock()
        mock_claude.execute_prompt.return_value = "保存完了"
        client._claude = mock_claude
        return client, mock_claude

    def test_marker_with_ellipsis_in_prompt(self):
        """'...' を含む after_marker がプロンプトに selection_with_ellipsis として反映される"""
        client, mock_claude = self._make_client()
        marker = "::: callout...:::"

        client.insert_after_existing(
            "https://www.notion.so/test-page-aabbccdd11223344aabbccdd11223344",
            "# Test Content",
            after_marker=marker,
        )

        prompt_arg = mock_claude.execute_prompt.call_args[0][0]
        assert "::: callout...:::" in prompt_arg

    def test_marker_without_ellipsis_builds_selection(self):
        """'...' を含まない after_marker から selection_with_ellipsis が構築される"""
        client, mock_claude = self._make_client()
        marker = "some-unique-block-text"

        client.insert_after_existing(
            "https://www.notion.so/test-page-aabbccdd11223344aabbccdd11223344",
            "# Test Content",
            after_marker=marker,
        )

        prompt_arg = mock_claude.execute_prompt.call_args[0][0]
        # [:15]="some-unique-blo", [-15:]="ique-block-text"
        assert "some-unique-blo...ique-block-text" in prompt_arg

    def test_none_marker_uses_append_path(self):
        """after_marker=None は末尾追記パスを使う"""
        client, mock_claude = self._make_client()

        client.insert_after_existing(
            "https://www.notion.so/test-page-aabbccdd11223344aabbccdd11223344",
            "# Test Content",
            after_marker=None,
        )

        prompt_arg = mock_claude.execute_prompt.call_args[0][0]
        assert "末尾" in prompt_arg
        assert "selection_with_ellipsis" not in prompt_arg.split("手順:")[0]

    def test_ambiguous_result_returns_false(self):
        """成功/失敗キーワードがない場合は False を返す（曖昧な成功扱い防止）"""
        client, mock_claude = self._make_client()
        mock_claude.execute_prompt.return_value = "何か別の出力テキスト"

        result = client.insert_after_existing(
            "https://www.notion.so/test-page-aabbccdd11223344aabbccdd11223344",
            "# Test Content",
        )
        assert result is False

    def test_explicit_failure_returns_false(self):
        """明示的な失敗キーワードがある場合は False を返す"""
        client, mock_claude = self._make_client()
        mock_claude.execute_prompt.return_value = "保存失敗: ページが見つかりません"

        result = client.insert_after_existing(
            "https://www.notion.so/test-page-aabbccdd11223344aabbccdd11223344",
            "# Test Content",
        )
        assert result is False


