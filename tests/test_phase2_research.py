        """標準マーカー「## 事前調査結果」で抽出できる"""
        output = (
            "調査を開始します。\n\n"
            "## 事前調査結果\n\n"
            "### タスク概要\nテスト用タスク\n\n"
            "Generated by `/task-research`\n\n"
            "他に作業はありますか？"
        )
        result = _extract_research_report(output)
