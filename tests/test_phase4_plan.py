class TestExtractDevPlanContent:
    """_extract_dev_plan_content のテスト"""

    def test_with_heading(self):
        """見出しありで正しく抽出"""
        output = """はい、開発計画を作成します。

## 開発計画

- [ ] **1.1** APIスキーマ変更
- [ ] **1.2** バックエンド実装

### 変更予定ファイル
- src/api.ts
"""
        result = _extract_dev_plan_content(output)

        assert "## 開発計画" in result
        assert "1.1" in result
        assert "はい、開発計画を作成します" not in result

    def test_no_heading(self):
        """見出しなしで raw output を返す"""
        output = "LLMからの応答テキストでマーカーなし"
        result = _extract_dev_plan_content(output)

        assert result == output

    def test_expanded_markers_checklist(self):
        """「実装チェックリスト」マーカーでマッチ"""
        output = """前置きテキスト

## 実装チェックリスト

- [ ] **1.1** 機能A
- [ ] **2.0** 機能B
"""
        result = _extract_dev_plan_content(output)

        assert "## 実装チェックリスト" in result
        assert "前置きテキスト" not in result

    def test_expanded_markers_plan_points(self):
        """「計画のポイント」マーカーでマッチ"""
        output = """概要です。

### 計画のポイント

1.1 最初のステップ
2.0 次のステップ
"""
        result = _extract_dev_plan_content(output)

        assert "### 計画のポイント" in result
        assert "概要です。" not in result

    def test_end_marker_generated_by(self):
        """Generated by で切断される"""
        output = """## 開発計画

- [ ] **1.1** 機能A

Generated by Claude
"""
        result = _extract_dev_plan_content(output)

        assert "1.1" in result
        assert "Generated by" in result


class TestValidateWorkPlan:
    """_validate_work_plan のテスト"""

    def test_valid_checklist(self):
        """有効なチェックリスト"""
        text = """## 開発計画
- [ ] **1.1** APIスキーマ変更
- [ ] **1.2** バックエンド実装
- [ ] **2.0** フロントエンド実装
- [ ] **3.0** テスト追加
"""
        is_valid, warnings = _validate_work_plan(text)
        assert is_valid is True
        assert warnings == []

    def test_no_steps(self):
        """ステップ不足で warning"""
        text = "ただのテキストです。ステップがありません。"
        is_valid, warnings = _validate_work_plan(text)
        assert is_valid is False
        assert any("ステップ番号が少なすぎます" in w for w in warnings)

    def test_no_checkboxes_no_numbered(self):
        """チェックボックスもステップ番号もなしで warning"""
        text = "単なるテキスト"
        is_valid, warnings = _validate_work_plan(text)
        assert is_valid is False
        assert any("チェックリスト形式" in w for w in warnings)

    def test_numbered_without_checkboxes(self):
        """番号付きだがチェックボックスなし — ステップ数十分なら有効"""
        text = """1.1 最初のステップ
1.2 次のステップ
2.0 三番目のステップ
"""
        is_valid, warnings = _validate_work_plan(text)
        assert is_valid is True
        assert warnings == []

    def test_few_steps_warning(self):
        """ステップ数が少ない場合は warning"""
        text = """- [ ] **1.1** 唯一のステップ
"""
        is_valid, warnings = _validate_work_plan(text)
        assert is_valid is False
        assert any("少なすぎます" in w for w in warnings)


